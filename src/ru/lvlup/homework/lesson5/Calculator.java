package ru.lvlup.homework.lesson5;

public class Calculator {

        public int sum(int a, int b){ return a + b; }
        public double sum(double a, double b){ return a + b; }
        public long sum(long a, long b){ return a + b; }


        public int sub(int a, int b){ return a - b; }
        public double sub(double a, double b){ return a - b; }
        public long sub(long a, long b){ return a - b; }

        public int div(int a, int b){ return a / b; }
        public double div(double a, double b){ return a / b; }
        public long div(long a, long b){ return a / b; }

        public int multi(int a, int b){ return a * b; }
        public double multi(double a, double b){ return a * b; }
        public long multi(long a, long b){ return a * b; }

}
